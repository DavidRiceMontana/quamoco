package de.quamoco.qm.provider.util;

import java.util.Collection;

import org.eclipse.emf.common.notify.AdapterFactory;
import org.eclipse.emf.common.util.ResourceLocator;
import org.eclipse.emf.ecore.EStructuralFeature;
import org.eclipse.emf.edit.provider.ItemPropertyDescriptor;

/**
 * An {@link ItemPropertyDescriptor} that avoids the construction of loops in
 * the model by removing the context element from the choice of values.
 * 
 * @author herrmama
 * @author $Author: lochmann $
 * @version $Rev: 4974 $
 * @levd.rating YELLOW Hash: 4C65C13DF645118C5E22F0A4E5F74FD1
 */
public class LoopAvoidingItemPropertyDescriptor extends ItemPropertyDescriptor {

	/** Constructor. */
	public LoopAvoidingItemPropertyDescriptor(AdapterFactory adapterFactory,
			ResourceLocator resourceLocator, String displayName,
			String description, EStructuralFeature feature, boolean isSettable,
			boolean multiLine, boolean sortChoices, Object staticImage,
			String category, String[] filterFlags) {
		super(adapterFactory, resourceLocator, displayName, description,
				feature, isSettable, multiLine, sortChoices, staticImage,
				category, filterFlags);
	}

	/** {@inheritDoc} */
	@Override
	public Collection<?> getChoiceOfValues(Object object) {
		Collection<?> choiceOfValues = super.getChoiceOfValues(object);
		choiceOfValues.remove(object);
		return choiceOfValues;
	}
}
