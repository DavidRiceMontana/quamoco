package de.quamoco.qm.editor.hierarchyviews;

/**
 * Interface to determine the parent in a hierarchy.
 * 
 * @author herrmama
 * @author $Author: lochmann $
 * @version $Rev: 4974 $
 * @levd.rating YELLOW Hash: 6602ED481550074FDE80940152F0FA35
 */
public interface IParentProvider extends IHierarchyProvider {

	/** Determine whether a parent is provided for a certain element. */
	boolean providesParent(Object element);

	/**
	 * Get the parent for an element. This method is only called if
	 * {@link #providesParent} evaluates to true.
	 */
	Object getParent(Object element);

}